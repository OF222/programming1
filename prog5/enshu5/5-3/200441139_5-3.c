#include <stdio.h>
int main(void) {
	int pattern[16][16] = {{0,0,0,0,1,2,0,3,2,2,3,3,2},     //要素が0～3の16×16の配列を宣言
						  {0,0,3,0,1,1,2,0,3,2,2,3,3,2},
						  {2,1,2,3,3,0,1,2,0,2,3,3,1,0,2,3},
						  {0,0,0,0,1,2,0,3,2,2,3,3,2},
						  {0,0,3,0,1,1,2,0,3,2,2,3,3,2},
						  {2,1,2,3,3,0,1,2,0,1,0,2,3},
						  {0,0,0,0,1,2,0,1,3,2,3,2,2,3,3,2},
						  {0,0,3,0,1,1,2,0,3,2,2,3,3,2},
						  {2,2,2,0,3,1,1,2,2,0,1,0,2,3},
						  {0,0,0,0,1,2,0,3,2,2,3,3,2},
						  {0,0,3,0,3,1,2,0,3,2,2,3,3,2},
						  {2,1,2,2,3,0,0,2,0,1,0,2,3},
						  {0,0,0,0,1,2,0,0,3,2,2,3,3,2},
						  {0,0,3,0,1,1,2,0,2,2,2,1,3,2},
						  {2,1,2,3,3,0,1,2,0,1,0,2,3},
						  {0,0,0,0,1,2,0,2,1,0,2,3,3,1,2}};
	int i, j;  //ループカウンタの宣言

	//数字別に記号を対応させて表示する
	for (i = 0; i < 16; i++) {
		for (j = 0; j < 16; j++) {
			if (pattern[i][j] == 0) {
				printf(".");
			}
			if (pattern[i][j] == 1) {
				printf("+");
			}
			if (pattern[i][j] == 2) {
				printf("o");
			}
			if (pattern[i][j] == 3) {
				printf("#");
			}
		}
		printf("\n");  //一行表示し終えたら改行
	}
	return 0;
}


